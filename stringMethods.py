name = 'Ben'

#len() length of word
print(len(name))

#find character within string
print(name.find('e'))
print(name.find('N'))

#capitalize first letter
print(name.capitalize())

#all uppercase
print(name.upper())

#all lowercase
print(name.lower())

#is it only digits?
print(name.isdigit())

#is it only alphabetical characters?
print(name.isalpha())

#count how many chars are in string
print(name.count('e'))

#replace x with y in string
print(name.replace('B','N'))

#Print string 3 times
print(name*3)
